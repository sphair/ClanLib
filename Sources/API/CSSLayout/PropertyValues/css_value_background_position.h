#pragma once

#include "../CSSDocument/css_property_value.h"
#include "../CSSDocument/css_length.h"

namespace clan
{

class CSSValueBackgroundPosition : public CSSPropertyValue
{
public:
	CSSValueBackgroundPosition();
	void compute(const CSSValueBackgroundPosition *parent, CSSResourceCache *layout, float em_size, float ex_size);
	std::string to_string() const;
	void apply(CSSComputedValuesUpdater *updater);

	enum Type
	{
		type_value,
		type_inherit
	} type;

	enum Type1
	{
		type1_left,
		type1_center,
		type1_right,
		type1_percentage,
		type1_length
	};

	enum Type2
	{
		type2_top,
		type2_center,
		type2_bottom,
		type2_percentage,
		type2_length
	};

	class Position
	{
	public:
		Position() : type_x(type1_percentage), type_y(type2_percentage), percentage_x(0.0f), percentage_y(0.0f) { }

		Type1 type_x;
		Type2 type_y;

		CSSLength length_x;
		float percentage_x;
		CSSLength length_y;
		float percentage_y;
	};

	std::vector<Position> positions;
};

}
