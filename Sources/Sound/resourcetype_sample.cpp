/////////////////////////////////////////////////////////////////////////////
// ResourceData_Sample Construction:

ResourceData_Sample::ResourceData_Sample(class Resource &resource)
{
	DomElement &element = resource.get_element();

	SoundProvider *provider = 0;

	std::string name = resource.get_element().get_attribute("file");
	std::string sound_format = resource.get_element().get_attribute("format");
	bool streamed = (element.get_attribute("stream", "no") == "yes");

	provider = SoundProviderFactory::load(
		name,
		streamed,
		resource.get_manager().get_directory(resource), sound_format);

	if (provider)
	{
		soundbuffer = new SoundBuffer(provider);
	}
	else
	{
		throw Exception("Unknown sample format");
	}
}

ResourceData_Sample::~ResourceData_Sample()
{
	delete soundbuffer;
	soundbuffer = 0;
}

